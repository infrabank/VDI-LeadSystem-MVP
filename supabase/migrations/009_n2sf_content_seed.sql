### ⑥ N2SF-DA-3 — 단말 식별 및 인증

**무엇을:** 단말의 고유 식별자(MAC, TPM, 인증서 등)를 통해 단말을 식별하고, 등록된 단말만 시스템에 접근할 수 있도록 한다.

**왜:** 미등록 개인 단말로의 SaaS 접속은 기관이 통제 불가능한 데이터 흐름을 만든다.

**어떻게:**
- 기관 MDM에 등록된 단말 인증서 기반 접속 허용
- 비등록 단말 접속 시도 자동 차단 및 로그 기록

☐ 적용 / ☐ 부분 / ☐ 미적용

