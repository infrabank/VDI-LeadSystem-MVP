**무엇을:** SaaS 로그아웃 또는 비정상 세션 종료 시 연결된 모든 세션 식별자를 즉시 무효화한다.

**왜:** 세션 토큰이 무효화되지 않으면 토큰 탈취 후 재사용으로 지속적 접근이 가능하다.

**어떻게:**
