## 5. 복수 등급 혼재 시 상위 승계 규칙

하나의 정보시스템이 서로 다른 등급의 업무정보를 처리할 때는 **가장 높은 등급이 해당 시스템의 등급이 된다.**

| 혼재 조합 | 결정 등급 | 근거 |
|---|---|---|
| S + O | **S** | 상위 등급 우선 |
| C + S | **C** | 상위 등급 우선 |
| C + O | **C** | 상위 등급 우선 |
| C + S + O | **C** | 상위 등급 우선 |

> 다만, 하나의 정보시스템에 서로 다른 등급의 업무정보가 포함되는 경우에는 가장 높은 등급을 해당 정보시스템의 등급으로 분류한다.
> (N²SF 가이드라인 v1.0 본문 §2, 표 2-9 참조)

**실무 권고:** 낮은 등급 정보에 높은 등급 수준의 보안통제가 과도하게 적용되어 공공정보 활용이 제한될 수 있다. 가이드라인은 동일 등급별로 정보시스템을 **분리·운영**할 것을 권고한다. VDI 환경에서는 세션 격리 또는 가상 데스크톱 풀 분리가 이에 해당한다.

