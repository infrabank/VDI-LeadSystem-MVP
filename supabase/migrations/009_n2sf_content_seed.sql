## 1. 모델 3 시나리오 — 외부 SaaS 직접 연결

N²SF **정보서비스 모델 3**은 기관 전산망(S등급) 내 업무 단말이 인터넷 영역의 외부 SaaS 협업도구(일정·화상회의·메신저·자료공유 등)를 직접 사용하는 혼합 환경이다. 가이드라인은 이 시나리오에서 **업무협업 정보를 O등급으로 한정**할 것을 전제로 한다.

**핵심 위협 5가지:**
- S등급 단말에서 O등급 SaaS로 데이터 유출
- 사전 비승인 SaaS 무단 접속 / 공개 식별자 사용
- SaaS 연계체계 우회 접속
- 악성 SaaS 앱 설치 또는 변조
- 관리자 계정 탈취·취약점 악용

아래 12개 통제는 모델 3의 중점 통제(emphasis_controls) 5개와 VDI 공통 핵심 통제(VDI_CORE_CONTROLS)의 교집합, 그리고 모델 3 필수 통제 중 추가 권고 항목으로 구성했다.

