## 2. 핵심 통제 체크리스트 12선

### ① N2SF-EB-5 — 통신 경유(proxy) 강제화

**무엇을:** 모든 SaaS 접속 트래픽이 기관이 인가한 프록시 시스템을 경유하도록 통신 경로를 강제화한다.

**왜:** 단말이 SaaS에 직접 접속하면 트래픽 가시성이 없어 정보 유출 탐지가 불가능하다.

**어떻게:**
- 기관 망 출구에 포워드 프록시(Forward Proxy) 또는 CASB 배치
- 단말 PAC 파일 또는 GPO를 통한 프록시 우회 차단

