## 4. O(공개) 등급 — 정의와 예시

> 공개정보(O)는 기밀정보(C) 및 민감정보(S) 이외의 모든 정보가 해당된다. 또한 관련 법령에 규정된 정보 공개 요건을 충족한 비공개 정보와 기간의 경과 등으로 비공개 필요성이 소멸된 정보도 공개정보(O) 등급으로 분류한다.
> (N²SF 가이드라인 v1.0 본문 §2)

**O등급 업무정보 예시:**
- 공공데이터포털에 이미 게시된 통계자료
- 기관 조직도 및 연락처 (홈페이지 게재용)
- 예산 및 결산 공시 자료
- 입법·정책 관련 공청회 자료집

O등급이라도 **SaaS 외부 전송 시에는 정보흐름 통제(N2SF-IF-14)를 적용**해야 한다. O등급 데이터가 S등급 정보시스템에서 생성된 경우, 흐름 통제 없이 외부로 나가는 것 자체가 위협 시나리오가 된다.

