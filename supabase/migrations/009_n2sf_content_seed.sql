### ⑦ N2SF-EB-2 — 네트워크 트래픽 필터링

**무엇을:** 인터넷 구간 트래픽을 필터링하여 악성 사이트 접속 및 비승인 SaaS로의 연결을 차단한다.

**왜:** 승인되지 않은 SaaS 서비스는 데이터 주권·보안 기준이 불명확하여 정보 유출 위험이 크다.

**어떻게:**
- URL 카테고리 필터링으로 승인 SaaS 목록 화이트리스트 관리
- DNS 필터링으로 비승인 SaaS 도메인 차단

