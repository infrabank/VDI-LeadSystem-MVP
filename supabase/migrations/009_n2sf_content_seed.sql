## 1. 왜 자가분류가 필요한가

국가 망 보안체계(N²SF)는 기관이 **업무정보의 중요도**에 따라 보안통제를 차등 적용하는 프레임워크다. 통제 항목을 선택하기 전에 반드시 C(기밀) · S(민감) · O(공개) 등급을 확정해야 한다. 등급이 잘못 설정되면 이후 위협식별·보안대책 수립·적절성 평가 전 단계가 부정확해지고, 국가정보원 보안성 검토 신청 자체가 지연된다.

N²SF 5단계 절차 중 **➋ C/S/O 등급분류**는 나머지 네 단계의 기초 데이터를 공급한다.

> 국가 망 보안체계는 각급기관의 정보화사업에 대한 자체 보안대책을 수립하는 데 활용하며 ➊준비, ➋C/S/O 등급분류, ➌위협식별, ➍보안대책 수립, ➎적절성 평가·조정의 5단계 절차로 수행한다.
> (N²SF 가이드라인 v1.0 본문 §2)

본 가이드는 정식 등급분류 작업을 시작하기 전, 담당자가 스스로 초안을 잡을 수 있도록 설계되었다. 자가분류 결과는 이후 정식 적절성 평가의 입력값이 된다.

