**무엇을:** SaaS 연계 단말 및 VDI 이미지에서 사용하지 않는 기능, 포트, 프로토콜, 소프트웨어, 서비스를 제거하거나 비활성화한다.

**왜:** 사용하지 않는 구성요소는 공격 표면을 확대하며, SaaS 연동 단말에서는 불필요한 플러그인이 데이터 유출 경로가 될 수 있다.

