### ⑩ N2SF-IF-1 — 정보흐름 동적 통제

**무엇을:** 정보시스템 간 정보 흐름을 정책 기반으로 동적으로 통제하여 비인가 흐름을 차단한다.

**왜:** SaaS 환경은 API 연동을 통한 자동 데이터 전송 경로가 복잡하므로, 흐름 통제를 정적이 아닌 동적으로 관리해야 한다.

**어떻게:**
- API 게이트웨이를 통한 SaaS 연동 트래픽 정책 적용
- 새로운 SaaS 연동 신청 시 정보보안 검토 절차 의무화

